<h1>Welcome to Storybook</h1>
<p>
	Storybook helps you build UI components in isolation from your app's business logic, data, and context.
	That makes it easy to develop hard-to-reach states. Save these UI states as <strong>stories</strong> to revisit during development, testing, or QA.
</p>
<p>
	Browse example stories now by navigating to them in the sidebar.
	View their code in the <code>src/stories</code> directory to learn how they work.
	We recommend building UIs with a
	<a href="https://componentdriven.org" target="_blank" rel="nofollow noopener noreferrer" class="sbdocs sbdocs-a css-19nbuh3"><strong>component-driven</strong></a>
	process starting with atomic components and ending with pages.  
</p>

